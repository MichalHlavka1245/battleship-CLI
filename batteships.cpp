bool player_coordinate_check(string coord, vector<string>& existing) {
    for (string x : existing) {
        if (x == coord)
            return false; // duplicate coordinate
    }
    return true; // coordinate is unique
}

bool valid_coordinate(string coord) {
    if (coord.length() != 2) return false;
    if (coord[0] < 'A' || coord[0] > 'D') return false;
    if (coord[1] < '1' || coord[1] > '4') return false;
    return true;
}

